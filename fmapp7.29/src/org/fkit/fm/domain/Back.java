package org.fkit.fm.domain;

public class Back {
	private Integer id;
	private String sname;
	private String sid;
	private String backName;
	private String backPhone;
	private String backdate;
	private String unifoType;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public String getSid() {
		return sid;
	}
	public void setSid(String sid) {
		this.sid = sid;
	}
	public String getBackName() {
		return backName;
	}
	public void setBackName(String backName) {
		this.backName = backName;
	}
	public String getBackPhone() {
		return backPhone;
	}
	public void setBackPhone(String backPhone) {
		this.backPhone = backPhone;
	}
	public String getBackdate() {
		return backdate;
	}
	public void setBackdate(String backdate) {
		this.backdate = backdate;
	}
	public String getUnifoType() {
		return unifoType;
	}
	public void setUnifoType(String unifoType) {
		this.unifoType = unifoType;
	}
	
	
}
